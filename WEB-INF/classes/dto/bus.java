package dto;

public class bus {
	private int bus_id;
	private int bus_stat;
	private String bus_start;
	private int route_id;
	private String bus_co;
	
	public int getBus_id() {
		return bus_id;
	}
	public void setBus_id(int bus_id) {
		this.bus_id = bus_id;
	}
	public int getBus_stat() {
		return bus_stat;
	}
	public void setBus_stat(int bus_stat) {
		this.bus_stat = bus_stat;
	}
	public String getBus_start() {
		return bus_start;
	}
	public void setBus_start(String bus_start) {
		this.bus_start = bus_start;
	}
	public int getRoute_id() {
		return route_id;
	}
	public void setRoute_id(int route_id) {
		this.route_id = route_id;
	}
	public String getBus_co() {
		return bus_co;
	}
	public void setBus_co(String bus_co) {
		this.bus_co = bus_co;
	}
	
	
}
