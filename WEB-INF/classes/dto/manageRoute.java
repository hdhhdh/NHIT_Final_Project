package dto;

public class manageRoute {
	
	public String start;
	public String end;
	public String start_time;
	public int grade;
	public int count_bus;
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getCount_bus() {
		return count_bus;
	}
	public void setCount_bus(int count_bus) {
		this.count_bus = count_bus;
	}
		
}
