package dto;

public class route {
	private int route_id;
	private String route_start;
	private String route_end;
	private String route_time;
	private int route_price;
	
	public int getRoute_id() {
		return route_id;
	}
	public void setRoute_id(int route_id) {
		this.route_id = route_id;
	}
	public String getRoute_start() {
		return route_start;
	}
	public void setRoute_start(String route_start) {
		this.route_start = route_start;
	}
	public String getRoute_end() {
		return route_end;
	}
	public void setRoute_end(String route_end) {
		this.route_end = route_end;
	}
	public String getRoute_time() {
		return route_time;
	}
	public void setRoute_time(String route_time) {
		this.route_time = route_time;
	}
	public int getRoute_price() {
		return route_price;
	}
	public void setRoute_price(int route_price) {
		this.route_price = route_price;
	}
	
	
}
