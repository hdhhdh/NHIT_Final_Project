package dto;

public class user {
	private String u_id;
	private String u_name;
	private int u_age;
	private int u_gen;
	private int u_money;
	
	public String getU_id() {
		return u_id;
	}
	public void setU_id(String u_id) {
		this.u_id = u_id;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public int getU_age() {
		return u_age;
	}
	public void setU_age(int u_age) {
		this.u_age = u_age;
	}
	public int getU_gen() {
		return u_gen;
	}
	public void setU_gen(int u_gen) {
		this.u_gen = u_gen;
	}
	public int getU_money() {
		return u_money;
	}
	public void setU_money(int u_money) {
		this.u_money = u_money;
	}
	
	
}
